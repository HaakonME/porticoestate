<H2>What is the phpGW KnowledgeBase?</H2>

<P>The phpGW KnowledgeBase is a database filled with snippets of useful information
which you put in it that you can search with a so called 'natural language query'. 
In normal English: you can just jot in any question you like and the system
will come up with good stuff. (Well, it will try).</P>

<H2>Why did you make this?</H2>

<P>The most important reason for making this system is one that everyone will
agree on: helping newbies get started with Blender. The discussion groups on blender.nl
are flooded with the same requests for information every day and hopefully this
knowledge base will reduce the stress there a bit.</P>

<P>On the other hand, while answering Blender support mail at Not a Number, we were also
writing the same stuff daily and it was time to make our work more efficient.</P>

<P>So: a good reason and a selfish reason, a good mix to motivate me :-)</P>

<H2>How do I search?</H2>

<P>Just answer any question you like and the fuzzy search algorithm
will do its best to come up with relevant answers. There are a few
limitations to this though:

<UL>
	<LI>Words with less than 4 characters are ignored (tip: type 'Macintosh' 
when you're looking for information on Macs</LI>
	<LI>The search engine rates words depending on their frequency of use
in the database. Common words have less value than rare words. Example: 'phpGroupWare' 
will have no value since it is present in almost every answer. 'admin' on the 
other hand has a high value.
	<LI>As a result of the fuzzy search algorithm, the search will <B>always</B> 
return results. If results have a score of zero you can be pretty sure it's not
what you're looking for.</LI>
</UL>

<H2>How do I add knowledge?</H2>

<P>If you think some knowledge is missing, just click on 'add'. Fill out the form 
and press 'save'. Your addition will not be immediately published - first an editor
will check to see if this is indeed new knowledge and if the text is sufficiently clear.

<P>Before you add knowledge, please <b>doublecheck</b> the database to see
if you are not duplicating some information. We really want to try to keep the system
clean and efficient.</P>

